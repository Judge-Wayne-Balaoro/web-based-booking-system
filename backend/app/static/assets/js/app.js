// --- 1. INITIALIZE CALENDAR & FETCH BLOCKED DATES ---
let calendarCheck, calendarBook;

async function initCalendar() {
    try {
        // Fetch actual confirmed dates from your FastAPI backend
        // (Assuming your public.py router has an endpoint like /api/bookings/reserved-dates)
        const response = await fetch('/api/bookings/reserved-dates'); 
        let reservedDates = [];
        if (response.ok) {
            const data = await response.json();
            // Adjust based on how your backend returns the dates
            reservedDates = data.dates || data; 
        }

        const config = {
            minDate: "today",
            disable: reservedDates,
            dateFormat: "Y-m-d"
        };

        calendarCheck = flatpickr("#event_date", config);
        calendarBook = flatpickr("#book_event_date", config);
    } catch (error) {
        console.error("Failed to load calendar dates:", error);
        // Fallback to basic calendar if server is unreachable
        calendarCheck = flatpickr("#event_date", { minDate: "today" });
        calendarBook = flatpickr("#book_event_date", { minDate: "today" });
    }
}

// Run on page load
document.addEventListener('DOMContentLoaded', initCalendar);

// --- 2. AVAILABILITY CHECKER BUTTON ---
document.getElementById('checkAvailabilityBtn').addEventListener('click', function() {
    const dateInput = document.getElementById('event_date').value;
    const resultDiv = document.getElementById('availabilityResult');
    const msg = document.getElementById('availabilityMessage');
    const proceedBtn = document.getElementById('proceedToBookBtn');

    if (!dateInput) {
        alert("Please tap the calendar to select a date.");
        return;
    }

    // Because Flatpickr already greys out taken dates from the DB, 
    // if they can select it, it's available!
    msg.innerHTML = `<span class="text-emerald-600 font-bold">✨ This date is open!</span>`;
    resultDiv.classList.remove('hidden');
    proceedBtn.classList.remove('hidden');
    
    // Auto-fill the form calendar with the checked date
    calendarBook.setDate(dateInput);
});

// --- 3. SUBMIT FORM TO FASTAPI ---
document.getElementById('bookingForm').addEventListener('submit', async function(e) {
    e.preventDefault();

    const btn = document.getElementById('submitBookingBtn');
    const errorDiv = document.getElementById('formError');
    btn.innerText = "Processing Reservation...";
    btn.disabled = true;
    errorDiv.classList.add('hidden');

    // We use FormData because we are sending an Image file (Receipt)
    const formData = new FormData();
    formData.append('guest_name', document.getElementById('guest_name').value);
    formData.append('contact_number', document.getElementById('contact_number').value);
    formData.append('email', document.getElementById('email').value);
    formData.append('pax', document.getElementById('pax').value);
    formData.append('event_date', document.getElementById('book_event_date').value);
    formData.append('payment_method', document.getElementById('payment_method').value);
    formData.append('deposit_amount', document.getElementById('deposit_amount').value);
    
    // Attach the file
    const receiptFile = document.getElementById('receipt').files[0];
    if (receiptFile) {
        formData.append('receipt', receiptFile);
    }

    try {
        // Send to your FastAPI backend
        const response = await fetch('/api/bookings', {
            method: 'POST',
            body: formData // No Content-Type header needed for FormData, the browser sets it
        });

        const result = await response.json();

        if (response.ok && result.success) {
            // Hide Form, Show Success Card
            document.getElementById('bookingForm').classList.add('hidden');
            const resultDiv = document.getElementById('bookingResult');
            resultDiv.classList.remove('hidden');
            
            // Display the real code generated by your backend
            document.getElementById('displayBookingCode').innerText = result.booking_code;
            resultDiv.scrollIntoView({ behavior: 'smooth', block: 'center' });
        } else {
            // Show backend error (e.g., "Date already taken")
            errorDiv.innerText = result.message || "Failed to process reservation.";
            errorDiv.classList.remove('hidden');
            btn.innerText = "Confirm Reservation";
            btn.disabled = false;
        }
    } catch (error) {
        console.error("Booking Error:", error);
        errorDiv.innerText = "Server connection failed. Please try again later.";
        errorDiv.classList.remove('hidden');
        btn.innerText = "Confirm Reservation";
        btn.disabled = false;
    }
});